package javacoding;

public class Unaryoperator {

	public static void main(String[] args) {
		
		int x = 50;
		
		int y;
		
		y = --x;
		
		System.out.println("y = "+y);
		
		y = x--;
		
		System.out.println("y = "+y);
		
        y = ++x;
		
		System.out.println("y = "+y);
		
		
        y = x++;
		
		System.out.println("y = "+y);

	}

}
